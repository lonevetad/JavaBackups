package games.old;

public class EquipmentAttributeModifier_OLD {
	int index, value;

	public EquipmentAttributeModifier_OLD(int index, int value) {
		super();
		this.index = index;
		this.value = value;
	}

	public int getIndex() {
		return index;
	}

	public int getValue() {
		return value;
	}

	public void setIndex(int index) {
		this.index = index;
	}

	public void setValue(int value) {
		this.value = value;
	}
}
